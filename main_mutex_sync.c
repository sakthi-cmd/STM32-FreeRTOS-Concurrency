void StartTask1(void *argument)
{
	 uint32_t temp;

	  for(int i = 0; i < 10000; i++)
	  {
		  osMutexAcquire(counterMutexHandle, osWaitForever);
	    temp = counter;
	    osThreadYield();

	    temp = temp + 1;
	    osThreadYield();

	    counter = temp;
	    osMutexRelease(counterMutexHandle);
	    osThreadYield();
	  }

    for(;;)
    {
        osDelay(500);
    }
}
