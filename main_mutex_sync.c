void StartTask3(void *argument)
{
    char msg[50];

    /* Infinite loop to periodically monitor the counter variable */
    for(;;)
    {
        // Thread-safe reading of the counter using its respective mutex
        osMutexAcquire(counterMutexHandle, osWaitForever);
        uint32_t current_counter = counter;
        osMutexRelease(counterMutexHandle);

        snprintf(msg, sizeof(msg), "Live Thread Counter = %lu\r\n", (unsigned long)current_counter);

        // Guard UART transmissions with your pre-defined uartMutex to ensure thread safety
        osMutexAcquire(uartMutexHandle, osWaitForever);
        HAL_UART_Transmit(&huart1, (uint8_t *)msg, strlen(msg), 100);
        osMutexRelease(uartMutexHandle);

        // Check every 500ms instead of spinning raw CPU cycles
        osDelay(500);
    }
}
